package JavaCode;

class DefaultInitialisation{
	int a;
	float f;
	String s;
	
}

public class DefaultInitialisationExample {

	public static void main(String[] args) {
		
		DefaultInitialisation di = new DefaultInitialisation();
		System.out.println(di.a);
		System.out.println(di.f);
		System.out.println(di.s);
		
				

	}

}
